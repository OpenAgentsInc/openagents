import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, relative, resolve, sep } from 'node:path'
import { fileURLToPath } from 'node:url'
import { format, resolveConfig } from 'prettier'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const defaultSource = resolve(repoRoot, '..', 'fireball', 'src', 'icon.ts')
const sourcePath = resolve(
  process.env.OMEGA_FIREBALL_ICON_SOURCE ?? defaultSource,
)
const targetPath = resolve(
  repoRoot,
  '..',
  '..',
  'packages',
  'ui',
  'src',
  'icon.ts',
)
const source = readFileSync(sourcePath, 'utf8')
const iconPattern =
  /\{\n\s*name: ('(?:\\.|[^'])*'),\n\s*svg: ('(?:\\.|[^'])*'),\n\s*\}/g
const decodeTsString = value => Function(`"use strict"; return ${value}`)()
const encodeTsString = value =>
  `'${value
    .replaceAll('\\', '\\\\')
    .replaceAll("'", "\\'")
    .replaceAll('\r', '\\r')
    .replaceAll('\n', '\\n')}'`

const icons = Array.from(source.matchAll(iconPattern), match => ({
  name: decodeTsString(match[1]),
  svg: decodeTsString(match[2]),
}))

if (icons.length === 0) {
  throw new Error(`No icons found in ${sourcePath}`)
}

const names = icons.map(icon => icon.name)
const uniqueNames = new Set(names)

if (uniqueNames.size !== names.length) {
  throw new Error('Fireball icon catalog contains duplicate names')
}

if (icons.length !== 755) {
  throw new Error(`Expected 755 Fireball icons, found ${icons.length}`)
}

const relativeSource = sourcePath.endsWith('/fireball/src/icon.ts')
  ? '../fireball/src/icon.ts'
  : relative(repoRoot, sourcePath).split(sep).join('/')
const lines = [
  `// Generated by scripts/sync-fireball-icons.mjs from ${relativeSource}.`,
  '// Do not edit icon data by hand.',
  "import { Array, Effect, Layer, Option, Schema as S } from 'effect'",
  "import * as Context from 'effect/Context'",
  "import type { Html } from 'foldkit/html'",
  "import { html } from 'foldkit/html'",
  '',
  'export const IconName = S.Literals([',
  ...names.map(name => `  ${encodeTsString(name)},`),
  '])',
  'export type IconName = typeof IconName.Type',
  '',
  'export type IconDefinition = {',
  '  readonly name: IconName',
  '  readonly svg: string',
  '}',
  '',
  `export const IconCount = ${icons.length}`,
  '',
  'export const Icons: ReadonlyArray<IconDefinition> = [',
  ...icons.flatMap(icon => [
    '  {',
    `    name: ${encodeTsString(icon.name)},`,
    `    svg: ${encodeTsString(icon.svg)},`,
    '  },',
  ]),
  ']',
  '',
  'export const iconNamed = (name: IconName): IconDefinition =>',
  '  Option.getOrThrowWith(',
  '    Array.findFirst(Icons, icon => icon.name === name),',
  '    () => new Error(`Missing icon: ${name}`),',
  '  )',
  '',
  'export const iconSvg = (name: IconName): string => iconNamed(name).svg',
  '',
  'export const iconView = <Message>(',
  '  name: IconName,',
  "  className = 'size-4 text-white/55',",
  '): Html => {',
  '  const h = html<Message>()',
  '',
  '  return h.span(',
  '    [',
  '      h.AriaHidden(true),',
  '      h.Class(',
  '        `inline-flex shrink-0 items-center justify-center ${className} [&>svg]:size-full [&>svg]:shrink-0`,',
  '      ),',
  '      h.InnerHTML(iconSvg(name)),',
  '    ],',
  '    [],',
  '  )',
  '}',
  '',
  'export type IconServiceShape = {',
  '  readonly named: (name: IconName) => Effect.Effect<IconDefinition>',
  '  readonly svg: (name: IconName) => Effect.Effect<string>',
  '  readonly view: (name: IconName, className?: string) => Effect.Effect<Html>',
  '}',
  '',
  'const liveIconService: IconServiceShape = {',
  '  named: name => Effect.sync(() => iconNamed(name)),',
  '  svg: name => Effect.sync(() => iconSvg(name)),',
  '  view: (name, className) => Effect.sync(() => iconView(name, className)),',
  '}',
  '',
  'export class IconService extends Context.Service<',
  '  IconService,',
  '  IconServiceShape',
  ">()('@openagentsinc/IconService') {",
  '  static readonly layer = Layer.succeed(IconService, liveIconService)',
  '}',
  '',
]

mkdirSync(dirname(targetPath), { recursive: true })
writeFileSync(
  targetPath,
  await format(`${lines.join('\n')}\n`, {
    ...(await resolveConfig(targetPath)),
    filepath: targetPath,
  }),
)
