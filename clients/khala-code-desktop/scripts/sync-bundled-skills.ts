#!/usr/bin/env bun
// Regenerates src/bun/khala-bundled-skill-content.generated.ts from the
// canonical checked-in skill sources under <repo root>/.agents/skills/.
// Run after editing a bundled SKILL.md:
//   bun clients/khala-code-desktop/scripts/sync-bundled-skills.ts
// The khala-bundled-skills test suite pins the generated module against the
// canonical files, so a stale artifact fails CI instead of shipping.
import { readFile, writeFile } from "node:fs/promises"
import { resolve } from "node:path"

const desktopRoot = resolve(import.meta.dir, "..")
const repoRoot = resolve(desktopRoot, "../..")

const bundledSkillNames = ["khala-fleet"] as const

const entries: string[] = []
for (const name of bundledSkillNames) {
  const sourcePath = resolve(repoRoot, ".agents/skills", name, "SKILL.md")
  const markdown = await readFile(sourcePath, "utf8")
  entries.push(`  {
    markdown: ${JSON.stringify(markdown)},
    name: ${JSON.stringify(name)},
  },`)
}

const generated = `// Generated by scripts/sync-bundled-skills.ts — do not edit by hand.
// Canonical sources live at <repo root>/.agents/skills/<name>/SKILL.md.
export type KhalaCodeDesktopBundledSkillContent = Readonly<{
  markdown: string
  name: string
}>

export const khalaCodeDesktopBundledSkillContents: readonly KhalaCodeDesktopBundledSkillContent[] = [
${entries.join("\n")}
]
`

const targetPath = resolve(desktopRoot, "src/bun/khala-bundled-skill-content.generated.ts")
await writeFile(targetPath, generated, "utf8")
process.stdout.write(`wrote ${targetPath} (${bundledSkillNames.length} skill(s))\n`)
