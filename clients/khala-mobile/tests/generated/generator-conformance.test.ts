import { existsSync, mkdtempSync, readFileSync, readdirSync, rmSync } from "node:fs"
import { tmpdir } from "node:os"
import { join } from "node:path"
import { describe, expect, test } from "bun:test"

import { khalaMobileGeneratedScreenBundles } from "../../src/qa/screen-bundles/generated-screen-bundles"

const mobileRoot = new URL("../../", import.meta.url).pathname
const fromRoot = (path: string): string => join(mobileRoot, path)

const generatedScreenFiles = (): readonly string[] =>
  readdirSync(fromRoot("src/screens"))
    .filter(file => file.endsWith("-screen.tsx"))
    .filter(file => readFileSync(fromRoot(`src/screens/${file}`), "utf8").includes("Generated by Khala mobile generator"))
    .map(file => `src/screens/${file}`)
    .sort()

describe("QAM-3 generator conformance", () => {
  test("generate screen emits the full test/story/contract/Maestro/visual bundle with no manual authoring", async () => {
    const tempRoot = mkdtempSync(join(tmpdir(), "khala-mobile-generate-"))
    try {
      const proc = Bun.spawn({
        cmd: ["bun", "run", "scripts/generate.ts", "screen", "Conformance Probe"],
        cwd: mobileRoot,
        env: {
          ...process.env,
          KHALA_MOBILE_GENERATE_OUT_DIR: tempRoot,
        },
        stderr: "pipe",
        stdout: "pipe",
      })
      const [exitCode, stderr] = await Promise.all([proc.exited, new Response(proc.stderr).text()])
      expect(stderr).toBe("")
      expect(exitCode).toBe(0)

      for (const path of [
        "src/screens/conformance-probe-screen.tsx",
        "tests/conformance-probe-screen.test.tsx",
        "src/screens/conformance-probe-screen.stories.tsx",
        "tests/conformance-probe-contract.test.ts",
        ".maestro/generated/conformance-probe-screen.yaml",
        "src/qa/visual-baselines/conformance-probe-screen.ts",
      ]) {
        expect(existsSync(join(tempRoot, path))).toBe(true)
      }
    } finally {
      rmSync(tempRoot, { force: true, recursive: true })
    }
  })

  test("every generated screen registered in source has its full bundle members", () => {
    const bundles = new Map(khalaMobileGeneratedScreenBundles.map(bundle => [bundle.screenFile, bundle]))

    expect(generatedScreenFiles()).toEqual([...bundles.keys()].sort())

    for (const bundle of khalaMobileGeneratedScreenBundles) {
      for (const artifact of [
        bundle.screenFile,
        bundle.mountTest,
        bundle.storyFile,
        bundle.contractTest,
        bundle.maestroFlow,
        bundle.visualRegistration,
      ]) {
        expect(existsSync(fromRoot(artifact))).toBe(true)
      }

      expect(readFileSync(fromRoot(bundle.mountTest), "utf8")).toContain("loading")
      expect(readFileSync(fromRoot(bundle.storyFile), "utf8")).toContain("Populated")
      expect(readFileSync(fromRoot(bundle.contractTest), "utf8")).toContain("blocker.")
      expect(readFileSync(fromRoot(bundle.maestroFlow), "utf8")).toContain("optional: true")
      expect(readFileSync(fromRoot(bundle.visualRegistration), "utf8")).toContain("viewport")
    }
  })
})
