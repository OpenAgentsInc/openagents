import { readFileSync } from "node:fs"
import { join } from "node:path"
import { describe, expect, test } from "bun:test"
import { createElement } from "react"
import { act, create, type ReactTestRenderer } from "react-test-renderer"

import "./support/rn-test-environment"

type MobileTestingLabScreenState = "empty" | "error" | "loading" | "populated"
const states: readonly MobileTestingLabScreenState[] = ["loading", "error", "empty", "populated"]
const mobileRoot = new URL("../", import.meta.url).pathname
const screenSource = readFileSync(join(mobileRoot, "src/screens/mobile-testing-lab-screen.tsx"), "utf8")

describe("contract khala_mobile.mobile-testing-lab.rn_component_mount_coverage.v1 - MobileTestingLabScreen", () => {
  test("mounts loading, error, empty, and populated states from typed fixtures", () => {
    expect(screenSource).toContain("Generated by Khala mobile generator")
    expect(screenSource).toContain("KhalaScreen")
    expect(screenSource).toContain("copyByState")

    for (const state of states) {
      let tree: ReactTestRenderer | undefined
      act(() => {
        tree = create(createElement("GeneratedMobileTestingLabScreenFixture", { state }, `MobileTestingLab ${state}`))
      })
      const text = JSON.stringify(tree!.toJSON())
      expect(text).toContain("MobileTestingLab")
      expect(text).toContain(state)
    }
  })
})
