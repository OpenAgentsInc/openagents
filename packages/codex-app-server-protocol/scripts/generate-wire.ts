import { writeFileSync } from "node:fs"
import { resolve } from "node:path"
import { Schema } from "effect"

const lane = process.env.CODEX_PROTOCOL_LANE
if (lane !== "current-source" && lane !== "bundled-0.144.1") {
  throw new Error("CODEX_PROTOCOL_LANE must select a generated protocol lane")
}

const meta = await import(`../src/_generated/${lane}/meta.gen.ts`)
type SchemaMap = Readonly<Record<string, Schema.Schema<unknown> | undefined>>

const documents = (schemas: SchemaMap): Readonly<Record<string, unknown>> => Object.fromEntries(
  Object.entries(schemas).map(([method, schema]) => [
    method,
    schema === undefined ? null : Schema.toJsonSchemaDocument(schema),
  ]),
)

const output = [
  "// Generated by @openagentsinc/codex-app-server-protocol. Do not edit manually.",
  `// Protocol lane: ${lane}`,
  "// Runtime JSON Schema documents generated from the reviewed Effect schemas.",
  "// @ts-nocheck -- public exports erase this generated structural graph.",
  `export const CLIENT_RESPONSE_DOCUMENTS: Readonly<Record<string, unknown>> = ${JSON.stringify(documents(meta.CLIENT_REQUEST_RESPONSES as SchemaMap))};`,
  `export const SERVER_REQUEST_DOCUMENTS: Readonly<Record<string, unknown>> = ${JSON.stringify(documents(meta.SERVER_REQUEST_PARAMS as SchemaMap))};`,
  `export const SERVER_NOTIFICATION_DOCUMENTS: Readonly<Record<string, unknown>> = ${JSON.stringify(documents(meta.SERVER_NOTIFICATION_PARAMS as SchemaMap))};`,
  "",
].join("\n")

writeFileSync(resolve(import.meta.dirname, "..", "src", "_generated", lane, "wire.gen.ts"), output)
console.log(`Generated ${lane} runtime wire decoders.`)
